<?php declare(strict_types=1);

namespace App\Tests;

class ArraySearchVsFor extends AbstractTestRunner
{
    public function __construct()
    {
        $input = self::getItems();

        $remove = 'apple';

        $this->addTest(
            new TestCase(
                'array_search - unset',
                function (array $array, string $remove) {
                    while (($index = array_search($remove, $array)) !== false) {
                        unset($array[$index]);
                    }

                    return array_values($array);
                },
                [$input, $remove]
            )
        );

        $this->addTest(
            new TestCase(
                'array_search - array_splice',
                function (array $array, string $remove) {
                    while (($index = array_search($remove, $array)) !== false) {
                        array_splice($array, $index, 1);
                    }

                    return $array;
                },
                [$input, $remove]
            )
        );

        $this->addTest(
            new TestCase(
                'for (backwards)',
                function (array $array, string $remove) {
                    for ($i = count($array) - 1; $i >= 0; $i--) {
                        if ($array[$i] == $remove) {
                            unset($array[$i]);
                        }
                    }

                    return array_values($array);
                },
                [$input, $remove]
            )
        );

        $this->addTest(
            new TestCase(
                'foreach',
                function (array $array, string $remove) {
                    foreach ($array as $i => $item) {
                        if ($item == $remove) {
                            unset($array[$i]);
                        }
                    }

                    return array_values($array);
                },
                [$input, $remove]
            )
        );
    }

